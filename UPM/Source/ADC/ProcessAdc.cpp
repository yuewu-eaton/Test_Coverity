// ********************************************************************************************************
// *
// * Function: LNtoLL
// *
// * Purpose:  calculates ll voltages from ln
// *
// * Parms Passed   :   pointer to ln voltages to convert
// *                    pointer to ll voltages for result
// * Returns        :   Nothing
// *
// * Description:
// *
// ********************************************************************************************************
#pragma CODE_SECTION( "ramfuncs" )
void LNtoLL( const stThreePhase* restrict ln, stThreePhase* restrict ll )
{
    ll->phA = ln->phA - ln->phB;
    ll->phB = ln->phB - ln->phC;
    ll->phC = ln->phC - ln->phA;
}
