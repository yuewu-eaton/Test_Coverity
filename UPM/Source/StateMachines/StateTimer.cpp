#include "DSP28x_Project.h"     // Device Headerfile and Examples Include File
#include "StateTimer.h"

// ***********************************************************************
// *
// *    FUNCTION: CheckTimeout 
// *
// * Parms Passed   :   timeout_value
// * Returns        :   TRUE/FALSE
// *
// * Description: This module will increment the count value.
// *              It returns true when the count value is >= timeout_value.
// *
// ***********************************************************************
#pragma CODE_SECTION( "ramfuncs" )
bool StateTimer::CheckTimeout( const uint32_t timeout )
{
    if ( timer < timeout )
    {
        ++timer;
        return timer == timeout;
    }
    else
    {
        return true;
    }
}


